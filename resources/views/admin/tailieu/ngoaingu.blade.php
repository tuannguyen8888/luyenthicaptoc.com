					Một câu (a sentence) là một nhóm các từ bao gồm một chủ ngữ (a subject) và một động từ (a verb) và diễn đạt một ý hoàn chỉnh (a complete thought). Một câu bắt đầu bằng một chữ viết hoa và kết thúc bằng dấu chấm. (Một câu cũng có thể được kết thúc bằng dấu hỏi chấm hoặc dấu chấm than, nhưng trong viết học thuật thì các câu được kết thúc bằng dấu chấm). Trong khuôn khổ bài học này sẽ không đề cập đến các câu đặc biệt như câu mệnh lệnh, câu cảm thán hay câu hỏi vì những dạng câu này không nằm trong dạng viết đoạn văn học thuật ở đây.<br><br>
