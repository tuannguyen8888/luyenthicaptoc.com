					<h4><b>II. Chủ ngữ, động từ, và tân ngữ (subjects, verbs, and objects)</b></h4><br>

					Chủ ngữ (a subject) có thể là một danh từ hoặc đại từ cho người nghe/đọc biết chủ thể của một hành động: ai hay cái gì làm gì?.<br>
					Động từ (a verb) thường có chức năng báo cho ta biết một hành động, hoặc đôi khi động từ chỉ có chức năng nối chủ ngữ với các thành phần còn lại của câu. Với chức năng đầu tiên, động từ được gọi là động từ hành động (action verbs), ví dụ như tell (nói), laugh (cười), run (chạy), drink (uống), v.v... Còn với chức năng thứ hai, động từ được gọi là động từ nối (linking verbs), ví dụ như to be (thì, là), become (trở nên), look (nhìn/trông có vẻ), seem (dường như/có vẻ như), v.v...<br><br>
					Với động từ hành động (action verbs) thì lại chia ra làm nội động từ (intransitive verbs) và ngoại động từ (transitive verbs). Nội động từ (ví dụ: cough, laugh, smile, sneeze) bản thân đã rõ nghĩa và không cần tân ngữ theo sau. Còn ngoại động từ (ví dụ: eat, drink, hit, watch) thì luôn cần một tân ngữ để làm rõ nghĩa (eat what? drink what? hit what/whom? watch what?)<br><br>
 					Tân ngữ (an object) là một danh từ (a person, a cat, a dog...) hoặc đại từ tân ngữ (me, you, him, her, us, them) và tiếp nhận hành động của ngoại động từ (transitive verbs). Tân ngữ KHÔNG đứng sau động từ nối (linking verbs) và không phải câu nào cũng cần tân ngữ. <br>
 					
